# Copied from transformers.models.bert.modeling_bert.BertLayer with Bert->Roberta
class RobertaLayer(nn.Module):
    def __init__(self, config):
        super().__init__()
        self.chunk_size_feed_forward = config.chunk_size_feed_forward
        self.seq_len_dim = 1
        self.attention = RobertaAttention(config)
        self.is_decoder = config.is_decoder
        self.add_cross_attention = config.add_cross_attention
        if self.add_cross_attention:
            assert self.is_decoder, f"{self} should be used as a decoder model if cross attention is added"
            self.crossattention = RobertaAttention(config)
        self.intermediate = RobertaIntermediate(config)
        self.output = RobertaOutput(config)

    def forward(
        self,
        hidden_states,
        attention_mask=None,
        head_mask=None,
        encoder_hidden_states=None,
        encoder_attention_mask=None,
        past_key_value=None,
        output_attentions=False,
        output_qkvs=False,
        output_length=None,
        always_keep_cls_token=True,
        layer_id=None,
    ):
        # decoder uni-directional self-attention cached key/values tuple is at positions 1,2
        self_attn_past_key_value = past_key_value[:2] if past_key_value is not None else None
        self_attention_outputs = self.attention(
            hidden_states,
            attention_mask,
            head_mask,
            output_attentions=output_attentions,
            output_qkvs=output_qkvs,
            past_key_value=self_attn_past_key_value,
        )
        # self_attention_outputs: hidden_rep, attention_scores, (q, k, v)
        attention_output = self_attention_outputs[0]
        outputs = self_attention_outputs[1:]  # add self attentions if we output attention weights

        if output_length is not None:
            assert output_attentions
            attention_scores = self_attention_outputs[1]    # batch, head, lenq, lenk
            attention_probs = nn.Softmax(dim=-1)(attention_scores)
            significance_score = attention_probs.sum(2).sum(1) # batch, lenk
            if always_keep_cls_token:
                topk_indices = significance_score[:, 1:].topk((hidden_states.shape[1] - 1), dim=1)[1] + 1
                keep_indices = topk_indices[:, :output_length - 1]
                delete_indices = topk_indices[:, output_length - 1:]
                cls_index = keep_indices.new_zeros((keep_indices.size(0), 1))
                keep_indices = torch.cat((cls_index, keep_indices), 1) # batch, output_length
            else:
                topk_indices = significance_score.topk(hidden_states.shape[1], dim=1)[1]
                keep_indices = topk_indices[:, :output_length]
                delete_indices = topk_indices[:, output_length:]

            q, k, v = self_attention_outputs[-1]
            attention_output = expand_gather(attention_output, 1, keep_indices.unsqueeze(-1)) # batch, output_length, dim
            q_outputs = expand_gather(q, 1, keep_indices.unsqueeze(-1)) # batch, output_length, dim
            k_outputs = expand_gather(k, 1, keep_indices.unsqueeze(-1)) # batch, output_length, dim
            v_outputs = expand_gather(v, 1, keep_indices.unsqueeze(-1)) # batch, output_length, dim
            outputs = (outputs[0], (q_outputs, k_outputs, v_outputs))
        else:
            keep_indices = None
            delete_indices = None

        layer_output = apply_chunking_to_forward(
            self.feed_forward_chunk, self.chunk_size_feed_forward, self.seq_len_dim, attention_output
        )
        outputs = (layer_output,) + outputs
        return outputs, keep_indices, delete_indices

    def feed_forward_chunk(self, attention_output):
        intermediate_output = self.intermediate(attention_output)
        layer_output = self.output(intermediate_output, attention_output)
        return layer_output

def expand_gather(input, dim, index):
    size = list(input.size())
    size[dim] = -1
    return input.gather(dim, index.expand(*size))


# Copied from transformers.models.bert.modeling_bert.BertEncoder with Bert->Roberta
class RobertaEncoder(nn.Module):
    def __init__(self, config):
        super().__init__()
        self.config = config
        self.layer = nn.ModuleList([RobertaLayer(config) for _ in range(config.num_hidden_layers)])

    def forward(
        self,
        hidden_states,
        attention_mask=None,
        head_mask=None,
        encoder_hidden_states=None,
        encoder_attention_mask=None,
        past_key_values=None,
        use_cache=None,
        output_attentions=False,
        output_hidden_states=False,
        output_qkvs=False,
        return_dict=True,
        stop_layer=-1,
        layer_config=None,
        length_config=None,
        always_keep_cls_token=True,
    ):
        all_hidden_states = () if output_hidden_states else None
        all_self_attentions = () if output_attentions else None
        all_cross_attentions = () if output_attentions and self.config.add_cross_attention else None
        all_qkvs = []
        all_remain_indices = []
        all_remove_indices = []

        bsz, tsz, dim = hidden_states.size()
        if length_config is not None:
            restored_hidden_states = hidden_states
            remain_indices = torch.arange(tsz, device=hidden_states.device).unsqueeze(0).repeat(bsz, 1)
        next_decoder_cache = () if use_cache else None

        for i, layer_module in enumerate(self.layer):
            if layer_config is not None and i not in layer_config:
                continue

            if output_hidden_states:
                all_hidden_states = all_hidden_states + (hidden_states,)

            layer_head_mask = head_mask[i] if head_mask is not None else None
            past_key_value = past_key_values[i] if past_key_values is not None else None
            layer_output_length = length_config[i] if length_config is not None else None

            if getattr(self.config, "gradient_checkpointing", False) and self.training:
                if use_cache:
                    logger.warning(
                        "`use_cache=True` is incompatible with `config.gradient_checkpointing=True`. Setting "
                        "`use_cache=False`..."
                    )
                    use_cache = False

                def create_custom_forward(module):
                    def custom_forward(*inputs):
                        return module(*inputs, past_key_value, output_attentions, layer_output_length, always_keep_cls_token)
                    return custom_forward

                layer_outputs, keep_indices, delete_indices = torch.utils.checkpoint.checkpoint(
                    create_custom_forward(layer_module),
                    hidden_states,
                    attention_mask,
                    layer_head_mask,
                    encoder_hidden_states,
                    encoder_attention_mask,
                )
            else:
                layer_outputs, keep_indices, delete_indices = layer_module(
                    hidden_states,
                    attention_mask,
                    layer_head_mask,
                    encoder_hidden_states,
                    encoder_attention_mask,
                    past_key_value,
                    output_attentions,
                    output_qkvs,
                    output_length=layer_output_length,
                    always_keep_cls_token=always_keep_cls_token,
                    layer_id=i,
                )

            hidden_states = layer_outputs[0]
            if layer_output_length:
                all_remove_indices.append(remain_indices.gather(1, delete_indices))
                remain_indices = remain_indices.gather(1, keep_indices) # batch, output_length
                all_remain_indices.append(remain_indices)
                restored_hidden_states = restored_hidden_states.scatter(1,
                                                                        remain_indices.unsqueeze(-1).expand(-1, -1, dim),
                                                                        hidden_states)
                if attention_mask is not None:
                    attention_mask = expand_gather(attention_mask, 3, keep_indices.unsqueeze(1).unsqueeze(2))
                    if attention_mask.size(2) > 1:
                        attention_mask = expand_gather(attention_mask, 2, keep_indices.unsqueeze(1).unsqueeze(3))

            if use_cache:
                next_decoder_cache += (layer_outputs[-1],)
            if output_attentions:
                all_self_attentions = all_self_attentions + (layer_outputs[1],)
            if output_qkvs:
                all_qkvs.append(layer_outputs[-1])
            if i == stop_layer:
                break

        if output_hidden_states:
            all_hidden_states = all_hidden_states + (hidden_states,)

        last_hidden_state = restored_hidden_states if length_config is not None else hidden_states

        if not return_dict:
            return tuple(
                v
                for v in [
                    hidden_states,
                    next_decoder_cache,
                    all_hidden_states,
                    all_self_attentions,
                    all_cross_attentions,
                ]
                if v is not None
            )
        return BaseModelOutputWithPastAndCrossAttentions(
            last_hidden_state=last_hidden_state,
            past_key_values=next_decoder_cache,
            hidden_states=all_hidden_states,
            attentions=all_self_attentions,
            cross_attentions=all_cross_attentions,
            qkvs=tuple(all_qkvs),
            all_remain_indices=all_remain_indices,
            all_remove_indices=all_remove_indices
        )



# Copied from transformers.models.bert.modeling_bert.BertPooler
class RobertaPooler(nn.Module):
    def __init__(self, config):
        super().__init__()
        self.dense = nn.Linear(config.hidden_size, config.hidden_size)
        self.activation = nn.Tanh()

    def forward(self, hidden_states):
        # We "pool" the model by simply taking the hidden state corresponding
        # to the first token.
        first_token_tensor = hidden_states[:, 0]
        pooled_output = self.dense(first_token_tensor)
        pooled_output = self.activation(pooled_output)
        return pooled_output


class RobertaPreTrainedModel(PreTrainedModel):
    """
    An abstract class to handle weights initialization and a simple interface for downloading and loading pretrained
    models.
    """

    config_class = RobertaConfig
    base_model_prefix = "roberta"

    # Copied from transformers.models.bert.modeling_bert.BertPreTrainedModel._init_weights
    def _init_weights(self, module):
        """Initialize the weights"""
        if isinstance(module, nn.Linear):
            # Slightly different from the TF version which uses truncated_normal for initialization
            # cf https://github.com/pytorch/pytorch/pull/5617
            module.weight.data.normal_(mean=0.0, std=self.config.initializer_range)
            if module.bias is not None:
                module.bias.data.zero_()
        elif isinstance(module, nn.Embedding):
            module.weight.data.normal_(mean=0.0, std=self.config.initializer_range)
            if module.padding_idx is not None:
                module.weight.data[module.padding_idx].zero_()
        elif isinstance(module, nn.LayerNorm):
            module.bias.data.zero_()
            module.weight.data.fill_(1.0)


